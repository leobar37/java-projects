    private void btnProcesar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesar1ActionPerformed

        String texto;
        int longitud, cantA;
        
        //Entrada
        texto = txtTexto1.getText();
   
        //Proceso
        Mensaje obj = new Mensaje();
        obj.setTexto(texto);
        longitud = obj.hallarLongitud();
        cantA = obj.contarA();
        
        //Salida
        lblLongitud.setText("" + longitud);
        lblLetraA.setText(String.valueOf(cantA));
        
        
    }//GEN-LAST:event_btnProcesar1ActionPerformed

    private void btnProcesar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesar3ActionPerformed
        
        String texto, cadena ;
        int posIni, posFin;
        
        texto = txtTexto3.getText();
        posIni = Integer.parseInt(txtPosIni.getText());
        posFin = Integer.parseInt(txtPosFin.getText());
        
        Mensaje obj = new Mensaje ();
        obj.setTexto(texto);
        cadena = obj.obtenerCadena(posIni, posFin);
        lblTextoEncontrado.setText (cadena);
        
        
    }//GEN-LAST:event_btnProcesar3ActionPerformed

    private void btnProcesar4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesar4ActionPerformed

        String texto, cadena ;
        int rpta=0;
        
        texto = txtTexto4.getText();
        cadena = txtBuscar.getText();
        
        Mensaje obj = new Mensaje ();
        obj.setTexto(texto);
        
        rpta = obj.buscarCadena(cadena);
        
        if (rpta==-1){
            lblRpta.setText("Texto no encontrado");
        }
        else{
            lblRpta.setText("Texto encontrado en la posici√≥n " +  (rpta + 1) );
        }
        
        
        
    }//GEN-LAST:event_btnProcesar4ActionPerformed

