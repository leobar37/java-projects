    private void pikachuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pikachuActionPerformed
         if(obj.isCronometroActivo() == false){
              iniciarCronometro(); 
           }
         cont = cont + 1;
          if(obj.getSegundos() >= 30){
               JOptionPane.showMessageDialog(null,"finalizo tu tiempo");
                obj.setCronometroActivo(false);
                tiempoHecho.setText(obj.getTiempo().getText());
                clicksLogrados.setText(""+cont);
           }
           
        MouseListener mouse= new MouseListener() {
            @Override
            public void mouseClicked(MouseEvent me) {
                
               cont = cont +1;
            }

            @Override
            public void mousePressed(MouseEvent me) {
              repuesta.setText("me estas aplastando");
            }

            @Override
            public void mouseReleased(MouseEvent me) {
               repuesta.setText("por fin me dejaste crrano");
            }

            @Override
            public void mouseEntered(MouseEvent me) {
             repuesta.setText("me estas rozando");
            }

            @Override
            public void mouseExited(MouseEvent me) {
             repuesta.setText("no te vayas pes serrano");
            }
        };
         botn.addMouseListener(mouse);
        clicksEN.setText(""+cont);
       
                                       
        
        
    }//GEN-LAST:event_pikachuActionPerformed

