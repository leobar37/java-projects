    private void mostrarListado(Vector lista, JTable tabla){
        
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("modelo");
        modelo.addColumn("tipo");
        modelo.addColumn("marca"); 
        modelo.addColumn("nuevo precio");
        modelo.addColumn("saldo a pagar");
        
        
        Object datos[] = new Object[5]; 
        Vehiculo obj;
        
        for (int i=0;i<lista.size();i++){
            obj = (Vehiculo)lista.elementAt(i);
            
            datos[0] = obj.getModelo();
            datos[1] = obj.getTipo();
            datos[2] = obj.getMarca();
            datos[3] = obj.NuevoPrecio();
            datos[4] = obj.Saldo();
     
            modelo.addRow(datos);
        }
        
        
        tabla.setModel(modelo);
    }
