    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
       String  modelo, marcat, tipos;
       float precio, montoInicial;
       modelo = Modelo.getText();
       marcat = LBMarca.getText();
       tipos = lblTipo.getText();
      
       precio = Float.parseFloat(lblPrecio.getText());
       montoInicial = Float.parseFloat(lblMontoInicial.getText());
      Vehiculo obj = new Vehiculo();
       obj.setMarca(marcat);
       obj.setModelo(modelo);
       obj.setMontoInicial(montoInicial);
       obj.setPrecio(precio);
       obj.setTipo(tipos);
      
       obj.registrar();
       ExportarTXT();
        JOptionPane.showMessageDialog(null, "se registraron correctamnete los datos");
    }//GEN-LAST:event_btnGuardarActionPerformed
private String Convertir(Vector lista){
      Vehiculo obj;
        String txt = "";
        String line = System.getProperty("line.separator"); 
        for (int i=0;i<lista.size();i++){
            
            obj = (Vehiculo)lista.elementAt(i);
            txt = txt +obj.getMarca() +"%"+obj.getModelo()+"%"+obj.getTipo()+ line;
            
        }
        return txt;
 }
