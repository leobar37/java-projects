    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
     Connection conexion;
     try{
         conexion = geConnection();
         ps = conexion.prepareStatement("select * from persona where clave = ?");
         ps.setInt(1,Integer.parseInt(txtPass.getText()));
         rs=ps.executeQuery();
         if(rs.next()){
            idPersonatxt.setText(String.valueOf(rs.getInt("idpersona")));
             //(nombre,clave,apelliw7do,naciemiento,genero)
            txtApellido.setText(rs.getString("apellido"));
            txtNOMBRE.setText(rs.getString("nombre"));
            txtNacimiento.setText(""+rs.getDate("naciemiento"));
            jcboGenero.setSelectedItem(rs.getString("genero"));
            txtClave.setText(rs.getString("clave"));
            JOptionPane.showMessageDialog(null,"Registro encontrado");
         }else{
             JOptionPane.showMessageDialog(null,"Registro no encontrado");
         }
         conexion.close();
     }catch(Exception ex){
         System.err.println("Error, "+ex);  
     }
     
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void txtNacimientoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtNacimientoActionPerformed
     Connection conexion;
     try{
       conexion = geConnection();
       ps = conexion.prepareStatement("insert into persona(nombre,clave,apellido,naciemiento,genero) values (?,?,?,?,?)");
       ps.setString(1, txtNOMBRE.getText());
       ps.setInt(2,Integer.parseInt(txtClave.getText()));
       ps.setString(3, txtApellido.getText());
       ps.setDate(4, Date.valueOf(txtNacimiento.getText()));
       ps.setString(5, jcboGenero.getSelectedItem().toString());
       //actualizar la tabla de base de datos
       int resultado = ps.executeUpdate();
       if(resultado>0){
           JOptionPane.showMessageDialog(null,"registro guardado");
       }else{
           JOptionPane.showMessageDialog(null,"ha ocurrido un error");
       }
       conexion.close();
  
     }catch(Exception ex){
         System.err.println("Error, "+ex);
     }
        
        
    }//GEN-LAST:event_txtNacimientoActionPerformed

