    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
      Connection conexion;
     try{
       conexion = geConnection();
       //persona(nombre,clave,apellido,naciemiento,genero)
       ps = conexion.prepareStatement("update persona set nombre =?,clave=?,apellido=?,naciemiento=?,genero=? where idpersona=?");
       ps.setString(1, txtNOMBRE.getText());
       ps.setInt(2,Integer.parseInt(txtClave.getText()));
       ps.setString(3, txtApellido.getText());
       ps.setDate(4, Date.valueOf(txtNacimiento.getText()));
       ps.setString(5, jcboGenero.getSelectedItem().toString());
       ps.setInt(6,Integer.parseInt(idPersonatxt.getText()));
       int resultado = ps.executeUpdate();
       if(resultado>0){
           JOptionPane.showMessageDialog(null,"se modifico correctamente el registro");
           LimpiarFrame(panelEscuela);
       }else{
          JOptionPane.showMessageDialog(null,"no modifico correctamente el registro");  
       }
       conexion.close();
       
     }catch(Exception ex){
         
     }
     
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
      if(idPersonatxt.getText().equals("")){
        JOptionPane.showMessageDialog(null,"debe buscar una persona"
                                           + "\npara poder ELIMINAR");
      }else{
        Connection conexion;
     try{
       conexion = geConnection();
       //persona(nombre,clave,apellido,naciemiento,genero)
       ps = conexion.prepareStatement("delete from persona where idpersona=?");
       ps.setInt(1,Integer.parseInt(idPersonatxt.getText()));
       int resultado = ps.executeUpdate();
       if(resultado>0){
           JOptionPane.showMessageDialog(null,"se elimino correctamente el registro");
           LimpiarFrame(panelEscuela);
       }else{
          JOptionPane.showMessageDialog(null,"no se elimino correctamente el registro");  
       }
      conexion.close();
       
     }catch(Exception ex){
         System.err.println("Error, "+ex);  
     }
      }
     
    }//GEN-LAST:event_btnEliminarActionPerformed
