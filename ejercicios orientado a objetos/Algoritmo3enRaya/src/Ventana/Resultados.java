   public void Jugadores(Jugador j1,Jugador j2){
       DefaultTableModel modelo = new DefaultTableModel(){
           @Override
           public boolean isCellEditable(int row,int column){
             return false;  
           }
       };
    modelo.addColumn("nombre");
    modelo.addColumn("ganado");
    modelo.addColumn("perdido");
    Object[] fila = new Object[3];
    for(int i = 0;i<3;i++){
     fila[0]=j1.getNombre();
     fila[1]=j1.getPerdidas();
     fila[2]=j1.getGanadas(); 
     
    }
    modelo.addRow(fila);
   
    Object[] fila2 = new Object[3];
    for(int i = 0;i<3;i++){
     fila2[0]=j2.getNombre();
     fila2[1]=j2.getPerdidas();
     fila2[2]=j2.getGanadas();    
    }
    modelo.addRow(fila2);
    
    tablaR.setModel(modelo);
    
   }
   
